import time

print('Data e hora:', time.strftime('%d-%m-%Y-%H.%M.%S'))

print('Ano:', time.strftime('%Y'))

print('Mês:', time.strftime('%m'))
      
print('Semana do ano:', time.strftime('%W'))

print('Dia atual da semana:', time.strftime('%A'))

print('Dia do ano:', time.strftime('%j'))

print('Dia do mês:', time.strftime('%d'))

print('Dia da semana:', time.strftime('%w'))
